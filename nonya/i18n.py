"""Tiny stdlib-only i18n for nonya. English is the base; other locales fall back
to English, then to the key itself. Language resolution order:

  1. NONYA_LANG env (e.g. "ko", "ja", "zh-Hans", "es")
  2. the OS locale (LC_ALL / LC_MESSAGES / LANG, e.g. "ko_KR.UTF-8" -> "ko")
  3. "en"

t(key, *args) returns the localized template .format(*args)-applied. Keep all
user-facing strings here so the app can ship globally + offer a language picker.
"""
from __future__ import annotations

import os
import re

BASE = "en"
SUPPORTED = ("en", "ko", "ja", "zh-Hans", "zh-Hant", "es", "fr", "de", "pt-BR")


def _normalize(raw: str) -> str:
    if not raw:
        return ""
    s = raw.strip().replace("_", "-")
    s = s.split(".")[0]                      # drop ".UTF-8"
    low = s.lower()
    if low.startswith("zh"):
        # script-aware: zh-Hant/zh-TW/zh-HK -> Traditional, else Simplified
        if any(t in low for t in ("hant", "tw", "hk", "mo")):
            return "zh-Hant"
        return "zh-Hans"
    if low.startswith("pt"):
        return "pt-BR"
    return low.split("-")[0]                 # "en-US" -> "en"


_MAC_LANG = None   # cached macOS preferred language (computed once per process)


def _mac_pref_lang() -> str:
    """macOS preferred UI language via `defaults read -g AppleLanguages`. GUI-spawned
    processes have NO LANG/LC_* env, so without this a core on a Korean Mac would wrongly
    default to English. Cached (one subprocess/process); local only (no network)."""
    global _MAC_LANG
    if _MAC_LANG is not None:
        return _MAC_LANG
    _MAC_LANG = ""
    import sys
    if sys.platform == "darwin":
        try:
            import subprocess
            out = subprocess.run(["defaults", "read", "-g", "AppleLanguages"],
                                  capture_output=True, text=True, timeout=3).stdout
            m = re.search(r'"?([A-Za-z]{2}(?:[-_][A-Za-z]+)*)"?', out)
            if m:
                _MAC_LANG = m.group(1)
        except (OSError, ValueError, subprocess.SubprocessError):
            pass
    return _MAC_LANG


def resolve_lang() -> str:
    env = os.environ.get("NONYA_LANG", "")
    if env:
        n = _normalize(env)
        if n in SUPPORTED:
            return n
    for var in ("LC_ALL", "LC_MESSAGES", "LANG"):
        n = _normalize(os.environ.get(var, ""))
        if n in SUPPORTED:
            return n
    if not os.environ.get("NONYA_NO_OS_LANG"):   # tests set this for deterministic en-base
        n = _normalize(_mac_pref_lang())          # macOS: detect the system language when no env is set
        if n in SUPPORTED:
            return n
    return BASE


def t(key: str, *args) -> str:
    lang = resolve_lang()
    table = CATALOG.get(lang) or {}
    s = table.get(key)
    if s is None:
        s = CATALOG[BASE].get(key, key)
    try:
        return s.format(*args) if args else s
    except (IndexError, KeyError):
        return s


