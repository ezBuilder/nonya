def pick_nudge(sentinel: str = "<<DONE>>") -> str:
    """A randomly-rotated playful brand nudge (ko); other locales use the plain functional default.
    Keeps the teasing voice but stays actionable (continue + sentinel)."""
    import random
    if resolve_lang() == "ko":
        return random.choice(_KO_NUDGES).format(sentinel or "<<DONE>>")
    return t("nudge.default")


